// Example applied what we have learnt for variable section

#include <stdio.h>

int main() {
    int studentID = 89, studentAge = 22;
    char studentGrade = 'A';
    float tuitionFee = 28000.76;

    printf("Student ID: %d\n", studentID);
    printf("Student Age: %d\n", studentAge);
    printf("Student Grade: %c\n", studentGrade);
    printf("Tuition Fee: %f\n", tuitionFee);

    return 0;
}